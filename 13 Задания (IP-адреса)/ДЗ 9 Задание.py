# 11111111.11111111.11100000.000000000 # 224
# 11111111.11111111.11110000.000000000 # 240
# 11111111.11111111.11111000.000000000 # 248
# 11111111.11111111.11111100.000000000 # 252
# 11111111.11111111.11111110.000000000 # 254
# 11111111.11111111.11111111.000000000 # 255

from ipaddress import *

maski = [0, 128, 192, 224, 240, 248, 252, 254, 255]

for A in maski:
    setochka = ip_network(f'187.124.21.237/255.255.{A}.0', False)

    count = 0
    for ip in setochka:
        dvoich = bin(int(ip))[2:].zfill(32)

        if dvoich[0:16].count('1') >= dvoich[16:].count('1'):
            count += 1

    if len(list(setochka)) == count:
        print(A)